  export function haversineKm(lat1: number, lng1: number, lat2: number, lng2: number): number {
    const toRad = (deg: number) => (deg * Math.PI) / 180;
    const R = 6371;
    const dLat = toRad(lat2 - lat1);
    const dLng = toRad(lng2 - lng1);
    const a =
      Math.sin(dLat / 2) ** 2 +
      Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  }
  
  export function findStation(query: string): Station | null {
    const q = query.replace(/\s/g, "").replace(/역$/, "").toLowerCase();
    if (!q) return null;
  
    return (
      STATIONS.find((s) => s.name.replace(/역$/, "").toLowerCase() === q) ??
      STATIONS.find((s) => s.name.replace(/역$/, "").toLowerCase().startsWith(q)) ??
      STATIONS.find((s) => s.name.replace(/역$/, "").toLowerCase().includes(q)) ??
      null
    );
  }
  
  export function getMidpoint(stationA: Station, stationB: Station) {
    return {
      lat: (stationA.lat + stationB.lat) / 2,
      lng: (stationA.lng + stationB.lng) / 2,
    };
  }
  
  export function getNearestStation(lat: number, lng: number): Station {
    return STATIONS.reduce((nearest, station) => {
      const dist = haversineKm(lat, lng, station.lat, station.lng);
      const nearestDist = haversineKm(lat, lng, nearest.lat, nearest.lng);
      return dist < nearestDist ? station : nearest;
    });
  }
  
  export function estimateCommuteMinutes(
    homeStation: Station,
    workStation: Station,
    walkMinutes: number = 10
  ): number {
    if (homeStation.name === workStation.name) return walkMinutes + 5;
  
    const dist = haversineKm(
      homeStation.lat, homeStation.lng,
      workStation.lat, workStation.lng
    );
  
    const sharedLine = homeStation.lines.some((l) => workStation.lines.includes(l));
    const fastLines = ["신분당선", "GTX-A", "공항철도", "수인분당선"];
    const hasFastLine =
      homeStation.lines.some((l) => fastLines.includes(l)) &&
      workStation.lines.some((l) => fastLines.includes(l));
  
    let trainMinutes: number;
    if (hasFastLine && sharedLine) trainMinutes = dist * 1.5;
    else if (sharedLine) trainMinutes = dist * 2.0;
    else trainMinutes = dist * 2.3;
  
    const transferPenalty = sharedLine ? 0 : 8;
    const total = walkMinutes + 5 + 4 + trainMinutes + transferPenalty;
  
    return Math.round(Math.min(Math.max(total, 15), 120));
  }